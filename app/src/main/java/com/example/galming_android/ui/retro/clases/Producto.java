package com.example.galming_android.ui.retro.clases;

public class Producto {
    private int opProdId;
    private int opProdPrecio;
    private int opProdDescuento;
    private int opProdStock;

    public int getOpProdId() {
        return opProdId;
    }

    public void setOpProdId(int opProdId) {
        this.opProdId = opProdId;
    }

    public int getOpProdPrecio() {
        return opProdPrecio;
    }

    public void setOpProdPrecio(int opProdPrecio) {
        this.opProdPrecio = opProdPrecio;
    }

    public int getOpProdDescuento() {
        return opProdDescuento;
    }

    public void setOpProdDescuento(int opProdDescuento) {
        this.opProdDescuento = opProdDescuento;
    }

    public int getOpProdStock() {
        return opProdStock;
    }

    public void setOpProdStock(int opProdStock) {
        this.opProdStock = opProdStock;
    }
}
